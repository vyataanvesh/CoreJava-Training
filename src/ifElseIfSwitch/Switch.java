package ifElseIfSwitch;

public class Switch {

    public static void main(String args[]) {
        int i = 4;

        switch (i) {
            case (1):
                System.out.println("entered into switch case 1");
                break;

        case (2):
            System.out.println("entered into switch case 2");
            break;

        default:
            System.out.println("entered into default switch case");
            break;
        }
    }
}
