package operators;

public class AssignmentOperator {
    public static void main(String args[]){
        int i = 20;

        System.out.println(i += 5);
        System.out.println(i -= 5);
        System.out.println(i *= 5);
        System.out.println(i /= 5);
        System.out.println(i %= 3);
    }
}
