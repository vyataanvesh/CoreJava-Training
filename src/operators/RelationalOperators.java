package operators;

public class RelationalOperators {
    public static void main(String args[]){
        int a = 20;
        int b = 6;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
