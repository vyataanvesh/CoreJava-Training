package operators;

public class UnaryOperators {
    public static void main(String args[]){
        int i = 20;

        System.out.println(i);
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(i);

        System.out.println(i);
        System.out.println(--i);
        System.out.println(i--);
        System.out.println(i);
    }
}
